    private void bt_igualActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_igualActionPerformed
        // TODO add your handling code here:
        
        valor2 = Double.parseDouble(tb_resultado.getText());
        if(sinal == "soma"){
            tb_resultado.setText(String.valueOf(valor1 + valor2));
        }
        
        else if(sinal == "menos"){
            tb_resultado.setText(String.valueOf(valor1 - valor2));
        }
        
        else if(sinal == "vezes"){
            tb_resultado.setText(String.valueOf(valor1 * valor2));
        }
        
        else if(sinal == "divisao"){
            tb_resultado.setText(String.valueOf(valor1 / valor2));
        }
        
    }//GEN-LAST:event_bt_igualActionPerformed

