    public void verificaEmpate(){
        contadorPosicao = 0;
        for(contaum=0; contaum<3; contaum++){
            for(contadois=0; contadois<3; contadois++){
                if(posicaovetor [contaum] [contadois] == "x" | posicaovetor [contaum] [contadois] == "o"){
                    contadorPosicao += 1;
                }
            }
        }
    }
    public void verificaVencedor(){
        linha1 = posicaovetor [0] [0] + posicaovetor [0] [1] + posicaovetor [0] [2];
        linha2 = posicaovetor [1] [0] + posicaovetor [1] [1] + posicaovetor [1] [2];
        linha3 = posicaovetor [2] [0] + posicaovetor [2] [1] + posicaovetor [2] [2];
        
        coluna1 = posicaovetor [0] [0] + posicaovetor [1] [0] + posicaovetor [2] [0];
        coluna2 = posicaovetor [0] [1] + posicaovetor [1] [1] + posicaovetor [2] [1];
        coluna3 = posicaovetor [0] [2] + posicaovetor [1] [2] + posicaovetor [2] [2];
        
        diagonal1 = posicaovetor [0] [0] + posicaovetor [1] [1] + posicaovetor [2] [2];
        diagonal2 = posicaovetor [0] [2] + posicaovetor [1] [1] + posicaovetor [2] [0];
        
        if(linha1.equals("xxx") | linha2.equals("xxx") | linha3.equals("xxx") | linha3.equals("xxx") | coluna1.equals("xxx") | coluna2.equals("xxx") | coluna3.equals("xxx") | diagonal1.equals("xxx") | diagonal2.equals("xxx")){
            txtVisor.setText("X Venceu!");
            desativaBotoes();
        }
        
        else if(linha1.equals("ooo") | linha2.equals("ooo") | linha3.equals("ooo") | linha3.equals("ooo") | coluna1.equals("ooo") | coluna2.equals("ooo") | coluna3.equals("ooo") | diagonal1.equals("ooo") | diagonal2.equals("ooo")){ 
            txtVisor.setText("O Venceu!");
            desativaBotoes();
        }
        
        else if (contadorPosicao == 9){            
             txtVisor.setText("Velha!");     
    }
        
        
    }
    
    
    
    
