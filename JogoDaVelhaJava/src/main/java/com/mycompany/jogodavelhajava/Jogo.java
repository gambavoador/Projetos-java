    public void limparVetor(){
        for(contaum=0; contaum<3; contaum++){
            for(contadois=0; contadois<3; contadois++){
                posicaovetor [contaum] [contadois] = "";
            }
        }
    }
    
    
    public void verificarJogador(){
        if(player1 && player2){
            this.valor=1;
            player1=false;
            player2=true;
            txtVisor.setText("Agora é a vez do O");
                       
        }
        
        else if(player1 && player2 == false){
            this.valor=1;
            player1=false;
            player2=true;
            txtVisor.setText("Agora é a vez do O");
        }
        
        else{
            this.valor=2;
            player1=true;
            player2=false;
            txtVisor.setText("Agora é a vez do X");
        }
    }
    
